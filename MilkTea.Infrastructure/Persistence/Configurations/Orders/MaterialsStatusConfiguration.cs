using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;
using MilkTea.Domain.Entities.Orders;

namespace MilkTea.Infrastructure.Persistence.Configurations.Orders
{
    public class MaterialsStatusConfiguration : IEntityTypeConfiguration<MaterialsStatus>
    {
        public void Configure(EntityTypeBuilder<MaterialsStatus> builder)
        {
            builder.ToTable("materialsstatus");

            builder.HasKey(x => x.ID);
            builder.Property(x => x.ID).HasColumnName("ID");

            builder.Property(x => x.Name).HasColumnName("Name").IsRequired();
        }
    }
}
